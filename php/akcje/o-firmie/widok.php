    <h1 style="text-align: center;">
        Trochę informacji o naszej wypożyczalni
    </h1>
    <p style="text-align: justify;">Wypożyczalnia pod sosną rozpoczęła swoją działalność w listopadzie 2001 roku. Specjalizujemy się w krótko-, średnio- oraz długoterminowym wynajmie samochodów, finansowaniu ich zakupu oraz w kompleksowym zarządzaniu flotą samochodów będących własnością Klienta.	</p>
    <p style="text-align: justify;">Nasza firma ukierunkowana jest na jakość wykonywanych usług. Naszą największą zaletą jest doświadczenie i jeden z najdłuższych staży na rynku, umiejętność dostosowania się do potrzeb Klienta, elastyczność oraz wysoko wykwalifikowana kadra pracowników, posiadających wieloletnie doświadczenie w tej branży. Do każdego Klienta podchodzimy indywidualnie, analizując jego potrzeby i możliwości, i na tej podstawie proponujemy najbardziej optymalne rozwiązania.</p>
    <p style="text-align: justify;">Siedziba firmy znajduje się w Sosnowcu, natomiast dzięki rozbudowanej sieci punktów wynajmu krótkoterminowego pod marką Your Rent, jesteśmy w stanie realizować wynajmy na terenie całego kraju.</p>
    <p style="text-align: justify;">Nasze oddziały zlokalizowane są w największych miastach Polski: Gdańsk - Katowice - Kraków - Lublin - Poznań - Warszawa - Wrocław.</p>
    <p style="text-align: justify;">Nasze punkty obsługi zlokalizowane są we wszystkich miastach wojewódzkich w Polsce.</p>

